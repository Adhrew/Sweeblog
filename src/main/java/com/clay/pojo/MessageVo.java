package com.clay.pojo;

public class MessageVo {
	private Integer sender_id;
	private Integer accept_id;
	private Integer count;
	private Integer isByTime;
	public Integer getSender_id() {
		return sender_id;
	}
	public void setSender_id(Integer sender_id) {
		this.sender_id = sender_id;
	}
	public Integer getAccept_id() {
		return accept_id;
	}
	public void setAccept_id(Integer accept_id) {
		this.accept_id = accept_id;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	public Integer getIsByTime() {
		return isByTime;
	}
	public void setIsByTime(Integer isByTime) {
		this.isByTime = isByTime;
	}
	
}
